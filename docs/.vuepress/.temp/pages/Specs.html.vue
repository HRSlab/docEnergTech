<h2 id="_7-0-functionality" tabindex="-1"><a class="header-anchor" href="#_7-0-functionality" aria-hidden="true">#</a> 7.0 Functionality</h2>
<p>Functionality is how your site actually works. This could be anything about specific parts of the website that need additional explanation.</p>
<p>For example, if you have a signup page, what fields are required? What happens to an entry on a contact form?</p>
<p>Many sites require integrations with third-party APIs. If this is the case then these integrations should be outlined here in terms of how they will work and any additional information that is needed. A good example of an integration is showing a feed of latest Tweets on your site.</p>
<p>Here are some examples of functionality you may want to mention, depending on your project.</p>
<ol>
<li>e-Commerce functionality such as payment gateways</li>
<li>SSL – is this required and how it should be implemented</li>
<li>Multi-lingual capabilities</li>
<li>User roles and capabilities – more than 1 type of user role where users can have different permission etc.</li>
<li>Analytics and tracking</li>
<li>Specific functionality around search</li>
<li>Performance requirements</li>
</ol>
<h2 id="_8-0-accessibility" tabindex="-1"><a class="header-anchor" href="#_8-0-accessibility" aria-hidden="true">#</a> 8.0 Accessibility</h2>
<p>Web accessibility is the practice of building websites that work for anyone, regardless of technology, location, or ability.</p>
<p>There are standards called the “<a href="https://www.w3.org/WAI/standards-guidelines/wcag/" target="_blank" rel="noopener noreferrer">Web Content Accessibility Guidelines<ExternalLinkIcon/></a>” (WCAG) that have been developed to assist web developers in building more accessible websites.</p>
<p>All websites should strive to achieve the highest levels of accessibility, but if you have specific requirements around this, then outline these as part of your specification.</p>
<h2 id="_9-0-browser-support" tabindex="-1"><a class="header-anchor" href="#_9-0-browser-support" aria-hidden="true">#</a> 9.0 Browser support</h2>
<p>Websites can be viewed on a wide range of devices and browsers. It is important to know which of these browsers and devices need to be supported, as their technical requirements can vary.</p>
<p>In particular, if you require support for older browsers (typically Internet Explorer) this can add to the overall project cost.</p>
<p>This section should outline which browsers and devices the site should be tested on. Many specifications will require testing in the latest version of the most popular browsers (Chrome, Firefox, Safari, Edge) and then specify one or two versions of Internet Explorer 9e.g. IE 10&amp;11).</p>
