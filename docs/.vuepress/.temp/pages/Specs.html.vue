<h3 id="_5-2-content-types" tabindex="-1"><a class="header-anchor" href="#_5-2-content-types" aria-hidden="true">#</a> 5.2 Content types</h3>
<p>A website can contain many distinct types of content. At it’s most basic, there will usually be posts and pages. A page is timeless content, e.g. ‘About us’, whereas a post is chronological, e.g. a news or blog post.</p>
<p>Some other common examples of content types are:</p>
<ol>
<li>People</li>
<li>Products</li>
<li>Testimonials</li>
</ol>
<p>Content type data</p>
<p>For each content type, the data associated with that content type should be listed. For example, if there was a ‘Person’ content type they might require the following data:</p>
<ol>
<li>First name</li>
<li>Last name</li>
<li>Position</li>
<li>Bio</li>
<li>mail address</li>
<li>Phone number</li>
</ol>
<h3 id="_5-3-taxonomies" tabindex="-1"><a class="header-anchor" href="#_5-3-taxonomies" aria-hidden="true">#</a> 5.3 Taxonomies</h3>
<p>A taxonomy is a scheme of classification for your website content. You can set site-wide taxonomies to be used across all content types, or you can have taxonomies that are specific to certain content types.</p>
<p>For example, if you had a recipe website you might want a taxonomy of ‘meals’ where the taxonomy terms would be ‘breakfast’, ‘lunch’, ‘dinner’, ‘snacks’, ‘dessert’, etc. You could also have a taxonomy of ‘cuisine’, with terms such as ‘indian’, ‘british’, ‘french’, etc.</p>
<p>On a blog, the most common two taxonomies are ‘Categories’ and ‘Tags’.</p>
<p>There are two main types of taxonomy:</p>
<ol>
<li>Hierarchical – e.g. ‘Categories’</li>
<li>Non-hierarchical – e.g. ‘Tags’</li>
</ol>
<p>Another example might be an ‘Industry’ taxonomy, which you could assign to your ‘Blog’, ‘Client’, ‘Case study’, and ‘Service’ content types.</p>
<h3 id="_5-4-page-templates" tabindex="-1"><a class="header-anchor" href="#_5-4-page-templates" aria-hidden="true">#</a> 5.4 Page templates</h3>
<p>A page template is a specific layout of information. For example, your ‘Home’ page will probably look different to your ‘Contact’ page.</p>
<p>Some examples of common page templates are below:</p>
<ol>
<li>Home</li>
<li>Blog post</li>
<li>‘Our team’</li>
<li>News archive – lists all the sites news posts in reverse chronological order</li>
<li>Contact – may have a map and a form</li>
</ol>
<p>If you have designs (wireframes or mockups) for these page templates please include them here.</p>
