<h2 id="_5-0-content" tabindex="-1"><a class="header-anchor" href="#_5-0-content" aria-hidden="true">#</a> 5.0 Content</h2>
<p>Content structure, or Information Architecture (IA), is comprised of various parts and will depend on the complexity and size of your website content.</p>
<h3 id="_5-1-site-map" tabindex="-1"><a class="header-anchor" href="#_5-1-site-map" aria-hidden="true">#</a> 5.1 Site map</h3>
<p>This is usually provided as a diagram which shows the ‘tree’ type, hierarchical structure of the website pages. It can also include which ‘page template’ (see below) to use for each page and content type.</p>
<p>There are excellent tools available for creating website sitemaps. We love Gloomaps.</p>
