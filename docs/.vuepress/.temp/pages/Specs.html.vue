<p>Mobile designs (and possibly tablet sizes) should be provided along with the usual desktop designs.</p>
<h3 id="_6-3-design-as-part-of-the-project-scope" tabindex="-1"><a class="header-anchor" href="#_6-3-design-as-part-of-the-project-scope" aria-hidden="true">#</a> 6.3 Design as part of the project scope</h3>
