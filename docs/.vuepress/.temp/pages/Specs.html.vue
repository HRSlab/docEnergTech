<p>Often, adding timescales or deadlines is a good idea, as this can help keep the project development focused and on track.</p>
<p>An example of some typical web project milestones are:</p>
<ol>
<li>Wireframes</li>
<li>Designs</li>
<li>Development (Front and Back-end)</li>
<li>Testing and feedback</li>
<li>Go Live</li>
</ol>
