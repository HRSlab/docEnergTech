<h2 id="_11-0-ongoing-support-and-maintenance" tabindex="-1"><a class="header-anchor" href="#_11-0-ongoing-support-and-maintenance" aria-hidden="true">#</a> 11.0 Ongoing support and maintenance</h2>
<p>Websites need to be updated, maintained and improved over time. If you are using a platform such as WordPress, the code base will quickly deteriorate if not regularly updated. This can lead to performance, compatibility, and security issues.</p>
<p>In your specification, outline any maintenance and support requirements that you have.</p>
<h2 id="_12-0-assumptions" tabindex="-1"><a class="header-anchor" href="#_12-0-assumptions" aria-hidden="true">#</a> 12.0 Assumptions</h2>
<p>One of the most common problems that projects run into is that parties have made assumptions about who is responsible for certain tasks.</p>
<p>The classic example is who adds the content. Often people commissioning websites (rightly) assume that the web company will add all of the content. However, often this is not the case and the client receives an ’empty’ version of their website.</p>
<p>Your website specification document should include everything that is needed for this project to be completed successfully.</p>
<p>Some common assumptions to think about include:</p>
<ol>
<li>Content addition</li>
<li>Design and layout customisation options</li>
<li>Migrating the site to the live server</li>
<li>Ongoing maintenance</li>
<li>SEO</li>
<li>Hosting</li>
</ol>
<h2 id="_13-0-milestones" tabindex="-1"><a class="header-anchor" href="#_13-0-milestones" aria-hidden="true">#</a> 13.0 Milestones</h2>
